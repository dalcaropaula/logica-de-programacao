#°°°°°°°°°°°°°°°°°°     Tuplas, Listas, Dicionários e Strings      °°°°°°°°°°°°°°°°°°  
#suponha que voce quer realizar o somatório de diversos valores, porem, não sabe quantos valores serão somados.
#pode ser que sejam somente 2, 10, 100.. Como criar uma função capaz de receber um numero tão variavel de paramtros?

def soma(*num):
    soma=0
    print(f'Tupla: {num}')
    for i in num:
        soma += i 
    return soma

print(f'Resultado {soma(1,2)}')
print(f'Resultado {soma(1,2,3,4,5,6,7,8,9)}')

